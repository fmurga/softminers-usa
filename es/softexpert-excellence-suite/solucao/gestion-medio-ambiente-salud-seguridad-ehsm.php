													<p>
													</p>
													<p>Las ventajas competitivas sustentables de las organizaciones de la sociedad industrial en el período que va de 1850 a 1975 estaban relacionadas sólo a los beneficios de las economías de escala y de alcance, a la rápida asignación de tecnología, a los activos fijos y a la excelencia de la gestión eficaz de los activos y pasivos financieros. Se trataba de una economía dominada por activos tangibles en la que sólo se utilizaban indicadores financieros para realizar la gestión del rendimiento, lo que no ocurre actualmente debido a que las organizaciones transfirieron sus activos tangibles a activos intangibles.</p>
													<p>Para Kaplan y Norton (1997), las mediciones financieras se crearon para comparar períodos anteriores a partir de estándares internos de rendimiento y, a pesar de que presentan esa información de forma bastante coherente, esta visión financiera ya no es capaz de prever el futuro e incluso imposibilita la percepción de las primeras señales de problemas de relaciones, calidad y nuevas oportunidades. Esto se produce porque la rapidez de la evolución de la tecnología de la información hace que ocurran grandes cambios, lo que exige una cantidad de información mayor y nuevas capacidades para controlar los procesos organizacionales para asegurar la ventaja competitiva en la toma de decisiones operacionales y estratégicas en tiempo hábil.</p>
