												<div class="wpb_text_column bottom-row">
													<ul class="recursos-lista-ul">
														<li class="recursos-lista-descricao">Establece una infraestructura de riesgo que entrega datos de alta calidad, produce informes reglamentarios precisos y permite la gestión y monitoreo de los riesgos en tiempo real.</li>
														<li class="recursos-lista-descricao">Crea repositorio de riesgos, controles, actividades de mitigación y procedimientos operacionales estándar facilitando el reúso.</li>
														<li class="recursos-lista-descricao">Identifica, captura y administra los procesos más críticos de riesgo y conformidad con una solución que proporciona rastreabilidad y auditorías.</li>
														<li class="recursos-lista-descricao">Mide las exposiciones de riesgo de todos los tipos de riesgo y actualiza las mediciones en la frecuencia necesaria.</li>
														<li class="recursos-lista-descricao">Evalúa los riesgos considerando sus varias dimensiones utilizando criterios de impacto y probabilidad y poderosos workflows para garantizar el correcto uso de los datos</li>
														<li class="recursos-lista-descricao">Permite la aplicación de modelos de evaluación de riesgos cuantitativos y cualitativos, independiente del tipo.</li>
														<li class="recursos-lista-descricao">Permite la evaluación automática de los riesgos, con base en la información colectada sobre la frecuencia y el impacto de los eventos ocurridos.</li>
														<li class="recursos-lista-descricao">Proporciona evaluaciones y comparaciones del riesgo residual y riesgo inherente, con alertas proactivos cuando los límites son excedidos.</li>
														<li class="recursos-lista-descricao">Ofrece aplicación de árbol de decisión para la identificación de puntos críticos de control.</li>
														<li class="recursos-lista-descricao">Automatiza los ciclos de test de los controles y envía notificaciones automáticas sobre problemas significativos relacionados a los controles.</li>
														<li class="recursos-lista-descricao">Inicializa eventos (Incidentes, Problemas o Workflows) automáticamente de acuerdo a la evaluación del riesgo o del control, usando reglas totalmente configurables.</li>
														<li class="recursos-lista-descricao">Monitorea la eficacia de las actividades de mitigación, controles y políticas, así como los cambios en los riesgos y requerimientos a través de la gestión de tests, indicadores e incidentes.</li>
														<li class="recursos-lista-descricao">Ofrece mapas de calor <i>(heat maps) </i>para análisis y monitoreo de los riesgos. </li>
														<li class="recursos-lista-descricao">Permite una visión global de todas las actividades relacionadas a los riesgos a través de dashboards y portales personalizados.</li>
														<li class="recursos-lista-descricao">Comunica los resultados a través de informes y portales configurables, mapas de calor con desdoblamiento, tendencias, lista de los principales riesgos. Todo con acceso directo a todos los detalles.</li>
														<li class="recursos-lista-descricao">Se aplica a las diferentes dimensiones de los riesgos, como financiera, reputación, legal, seguridad, salud, medio ambiente y seguridad alimentaria.</li>
														<li class="recursos-lista-descricao">Permite extraer muestras a partir de métodos estadísticos para ser utilizadas en las pruebas de control.</li>
