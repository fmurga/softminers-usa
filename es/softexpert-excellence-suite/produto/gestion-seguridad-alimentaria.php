											<ul class="recursos-lista-ul">
												<li class="recursos-lista-descricao">Establece, monitoriza y controla el Programa de Prerrequisitos (PPRs) y sus procedimientos generales, como los Procedimientos Operacionales Estandarizados de Higienización (POPs) y las Buenas Prácticas de Fabricación (BPFs).</li>
												<li class="recursos-lista-descricao">Aplica y monitoriza todas las etapas preliminares y los siete principios del APPCC.</li>
												<li class="recursos-lista-descricao">Proporciona una identificación robusta y específica de los peligros, seguida de la aplicación de métodos flexibles para la evaluación de los riesgos.</li>
												<li class="recursos-lista-descricao">Posibilita la determinación de los Puntos Críticos de Control (PCCs) en los peligros significativos utilizando árboles de decisión inteligentes y automatizados.</li>
												<li class="recursos-lista-descricao">Monitoriza los límites críticos de los PCCs y envía alertas cuando los límites son sobrepasados.</li>
												<li class="recursos-lista-descricao">Clasifica los peligros en categorías como biológicos, físicos, químicos y alergénicos. Con eso, colabora para la creación de una base robusta y única de peligros.</li>
												<li class="recursos-lista-descricao">Relaciona cada peligro con el control preventivo apropiado y evalúa su eficacia.</li>
												<li class="recursos-lista-descricao">Soporta la creación de diagramas de flujo del APPCC de forma simple y rápida a través de una herramienta de modelaje de procesos.</li>
												<li class="recursos-lista-descricao">Soporta la gestión y la revisión de los planes de APPCC, manteniendo la información más actualizada siempre disponible.</li>
												<li class="recursos-lista-descricao">Permite el registro de incidentes de seguridad y de calidad en el momento en que ellos suceden.</li>
												<li class="recursos-lista-descricao">Mejora la eficiencia y la eficacia de todos los procesos de calidad (no conformidades, planes de acción, control de cambios, etc.)</li>
												<li class="recursos-lista-descricao">Controla toda la documentación crítica de los planes de seguridad alimentaria en un repositorio integrado.</li>
												<li class="recursos-lista-descricao">Planifica, distribuye y administra encuestas de conformidad y tests de auto evaluación de controles y procedimientos.</li>
